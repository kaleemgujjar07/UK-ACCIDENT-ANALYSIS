for df in casualty_dfs[1:]:
    df_casualty = df_casualty.merge(
        df,
        on=['accident_index', 'seq'],
        how='left',
        suffixes=('', '_dup')          # avoid duplicate column warnings
    )
    # Drop any duplicate columns that may have appeared (like casualty_age from risk)
    # We'll keep the first occurrence (from main) and drop the rest.
    dup_cols = [c for c in df_casualty.columns if c.endswith('_dup')]
