# =============================================================================

casualty_dfs = [df_main, df_spatial, df_demographic, df_risk]
for df in casualty_dfs:
    df['seq'] = df.groupby('accident_index', observed=True).cumcount()

print("Sequence columns added to all casualty‑level dataframes.")

# =============================================================================
# 3️⃣ Verify Row Counts Are Identical Across All Casualty Files
# =============================================================================

ref_counts = df_main.groupby('accident_index', observed=True).size()
all_match = True
for name, df in zip(['spatial', 'demographic', 'risk'], casualty_dfs[1:]):
    counts = df.groupby('accident_index', observed=True).size()
    if not ref_counts.equals(counts):
        print(f"⚠️  Row counts differ in {name} file!")
        all_match = False
if all_match:
    print("✅ All casualty files have identical row counts per accident.")

# =============================================================================
# 4️⃣ Merge All Casualty‑Level Files (Left Join on accident_index & seq)
# =============================================================================

